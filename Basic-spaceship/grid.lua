-- keep 32 pixels for cells to fit to screen
local cell = {
    width = 32,
    height = 32,
}

local buildedTiles = {}

function buildGridTileLocations(theGrid)
  tiles = {}
  for rowNum, row in ipairs(theGrid) do
    for columnNumber, tileValue in ipairs(theGrid[rowNum]) do
      if tileValue == 1 then -- add only tiles with value 1 of the grid
        table.insert(tiles, {
          x = (columnNumber -1) * cell.width + theGrid.x,
          y = (rowNum -1) * cell.height + theGrid.y,
          width = cell.width,
          height = cell.height,
        })
      end
    end
  end
  return tiles
end

local currentGrid = grid -- default
function gridModule.loadBuildedTiles(gridNumber)
  if(gridNumber == 1) then
    buildedTiles = buildGridTileLocations(grid)
    currentGrid = grid
  elseif(gridNumber == 2) then
    buildedTiles = buildGridTileLocations(grid2)
    currentGrid = grid2
  end
end

function gridModule.getBuildedTilesInCollisions()
  return buildedTiles
end

function gridModule.drawTilesOnGrid()
  for tileNum, tile in ipairs(buildedTiles) do
    love.graphics.setColor(0.7, 0.7, 0.7)
    love.graphics.rectangle("fill", tile.x, tile.y, cell.width, cell.height)
  end
end

return gridModule
